# Tipe Data di Python

# 1. Integer
# Tipe data ini digunakan untuk menyimpan bilangan bulat, baik positif maupun negatif.
# Contoh: 10, -5, 0
tipe_data_integer = 10

# 2. Float
# Tipe data ini digunakan untuk menyimpan bilangan desimal.
# Contoh: 10.5, -3.14, 0.0
tipe_data_float = 10.5

# 3. String
# Tipe data ini digunakan untuk menyimpan teks atau karakter.
# String diapit oleh tanda kutip tunggal (' ') atau ganda (" ").
# Contoh: "Hello, World!", 'Python'
tipe_data_string = "Hello, World!"

# 4. Boolean
# Tipe data ini hanya memiliki dua nilai: True atau False.
tipe_data_boolean = True

# 5. List
# Tipe data ini digunakan untuk menyimpan kumpulan data yang terurut.
# List dapat menyimpan berbagai tipe data dan dapat diubah (mutable).
# Contoh: [1, 2, 3], ["apple", "banana", "cherry"]
tipe_data_list = [1, 2, 3]

# 6. Tuple
# Mirip dengan list, tetapi bersifat tidak dapat diubah (immutable).
# Contoh: (1, 2, 3), ("apple", "banana", "cherry")
tipe_data_tuple = (1, 2, 3)

# 7. Dictionary
# Tipe data ini digunakan untuk menyimpan pasangan kunci-nilai (key-value pairs).
# Contoh: {"nama": "John", "umur": 30}
tipe_data_dictionary = {"nama": "John", "umur": 30}

# 8. Set
# Tipe data ini digunakan untuk menyimpan kumpulan data yang tidak terurut dan tidak memiliki duplikasi.
# Contoh: {1, 2, 3}, {"apple", "banana", "cherry"}
tipe_data_set = {1, 2, 3}

# 9. Complex
# Tipe data ini digunakan untuk menyimpan bilangan kompleks.
# Contoh: 1+2j, 3-4j
tipe_data_complex = 1 + 2j

# 10. Bytes
# Tipe data ini digunakan untuk menyimpan urutan byte.
# Contoh: b"Hello", b'\x00\x01\x02'
tipe_data_bytes = b"Hello"

# 11. Bytearray
# Mirip dengan bytes, tetapi dapat diubah (mutable).
# Contoh: bytearray(5), bytearray(b"Hello")
tipe_data_bytearray = bytearray(b"Hello")

# 12. Memoryview
# Tipe data ini digunakan untuk mengakses buffer internal dari objek byte.
# Contoh: memoryview(b"Hello")
tipe_data_memoryview = memoryview(b"Hello")

# Mencetak semua tipe data yang telah didefinisikan
print("Tipe Data String:", tipe_data_string)
print("Tipe Data Boolean:", tipe_data_boolean)
print("Tipe Data List:", tipe_data_list)
print("Tipe Data Tuple:", tipe_data_tuple)
print("Tipe Data Dictionary:", tipe_data_dictionary)
print("Tipe Data Set:", tipe_data_set)
print("Tipe Data Complex:", tipe_data_complex)
print("Tipe Data Bytes:", tipe_data_bytes)
print("Tipe Data Bytearray:", tipe_data_bytearray)
print("Tipe Data Memoryview:", tipe_data_memoryview)


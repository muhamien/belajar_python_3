# Contoh Nama Variabel di Python yang bisa di-running:

# 1. Menggunakan huruf kecil dan pisahkan kata dengan underscore (snake_case).
nama_depan = "Muhammad"
jumlah_barang = 5
harga_total = 15000.75

# 2. Nama variabel harus dimulai dengan huruf atau underscore (_), tidak boleh dimulai dengan angka.
_data_tersembunyi = "rahasia"
nilai_ujian = 85

# 3. Gunakan nama yang deskriptif dan jelas untuk merepresentasikan data yang disimpan.
panjang_sisi = 10
tinggi_bangunan = 50
jumlah_peserta = 100

# 4. Hindari penggunaan kata kunci yang sudah ada di Python sebagai nama variabel.
# Contoh yang salah: class, def, if
# class = "salah"  # Ini akan menyebabkan error

# 5. Bersifat case-sensitive, jadi 'variabel' dan 'Variabel' adalah dua variabel yang berbeda.
variabel = 10
Variabel = 20

# Cetak variabel untuk memastikan semuanya berjalan dengan baik
print(nama_depan, jumlah_barang, harga_total)
print(_data_tersembunyi, nilai_ujian)
print(panjang_sisi, tinggi_bangunan, jumlah_peserta)
print(variabel, Variabel)
